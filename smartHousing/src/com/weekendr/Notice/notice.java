package com.weekendr.Notice;

public class notice {
public String society_Id;
public String User_Id;
public String notice_Title;
public String notice_Description;
public String submitted_by;
public String getSociety_Id() {
	return society_Id;
}
public void setSociety_Id(String society_Id) {
	this.society_Id = society_Id;
}
public String getUser_Id() {
	return User_Id;
}
public void setUser_Id(String user_Id) {
	User_Id = user_Id;
}
public String getNotice_Title() {
	return notice_Title;
}
public void setNotice_Title(String notice_Title) {
	this.notice_Title = notice_Title;
}
public String getNotice_Description() {
	return notice_Description;
}
public void setNotice_Description(String notice_Description) {
	this.notice_Description = notice_Description;
}
public String getSubmitted_by() {
	return submitted_by;
}
public void setSubmitted_by(String submitted_by) {
	this.submitted_by = submitted_by;
}

}
