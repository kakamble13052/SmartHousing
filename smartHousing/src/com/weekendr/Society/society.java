package com.weekendr.Society;

public class society {
public String society_Id;
public String society_Name;
public String society_Address;
public double number_of_Building;
public String President_Name;
public String getSociety_Id() {
	return society_Id;
}
public void setSociety_Id(String society_Id) {
	this.society_Id = society_Id;
}
public String getSociety_Name() {
	return society_Name;
}
public void setSociety_Name(String society_Name) {
	this.society_Name = society_Name;
}
public String getSociety_Address() {
	return society_Address;
}
public void setSociety_Address(String society_Address) {
	this.society_Address = society_Address;
}
public double getNumber_of_Building() {
	return number_of_Building;
}
public void setNumber_of_Building(double number_of_Building) {
	this.number_of_Building = number_of_Building;
}
public String getPresident_Name() {
	return President_Name;
}
public void setPresident_Name(String president_Name) {
	President_Name = president_Name;
}
}
